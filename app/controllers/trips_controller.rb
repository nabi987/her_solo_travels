  def show
    @trip = Trip.find(params[:id])
    @location = Location.new
  end

  def new
    @trip = Trip.new
  end

  def create
    @trip = Trip.create(trip_params)
    @trip.user = current_user
    unless trip_params[:photo].present?
      random_default_image(@trip)
    end
    if @trip.save
      redirect_to trip_path(@trip)
    else
      render :new
    end
  end

